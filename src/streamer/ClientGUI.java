    public String timeFormater(long millis){
        String mss = String.format("%02d:%02d:%02d",
        TimeUnit.MILLISECONDS.toMinutes(millis) % TimeUnit.HOURS.toMinutes(1),
        TimeUnit.MILLISECONDS.toSeconds(millis) % TimeUnit.MINUTES.toSeconds(1),
        (millis/100)%100);
        return mss;
    }
    public void updateVars() {
        if(mode == modes.play)
            jButton1.setText("Pause");
        else jButton1.setText("Play");
        updateProgress();
        updateTime();
    }
    
    public void updateTime() {
        // Playing
        if(mode == modes.play){
            timer = new Thread(
                new Runnable(){
                    public void run(){
                        try{
                            while(true){
                                ms = timerLastAmount + player.getTime();
                                if(ms>timerMostCurrent) timerMostCurrent = ms;
                                //ms += timerLastAmount;
                                jLabel1.setText(timeFormater(ms));
                            }
                        }catch(Exception e){
                            JOptionPane.showMessageDialog(null, "Error1 displaying time resuming"+ e);
                        }
                    }
                }
            );
            timer.start();
        
        // PAUSING
        }else{
            try{
                timer.stop();
                timer.interrupt();
                timer = null;
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Error2 timer pausing "+ e);
            }
            System.out.println("timerLastAmount="+timerLastAmount);
            System.out.println("timerMostCurrent="+timerMostCurrent);
            System.out.println("ms="+ms);
            timerLastAmount = ms;
            timerMostCurrent = -1;
        }
    }
    public void updateProgress() {
        // Playing
        System.out.println("ms="+ ms);
        System.out.println("trlength="+ trackLength);
        if(mode == modes.play){
            progress = new Thread(
                new Runnable(){
                    public void run(){
                        try{
                            while(true){
                                progressAmount = (int)(ms*100) / trackLength;
                                jLabel2.setText(String.format("%02d",progressAmount) +"%");
                                jProgressBar1.setValue(progressAmount);
                            }
                        }catch(Exception e){
                            JOptionPane.showMessageDialog(null, "Error1 displaying progress resuming"+ e);
                        }
                    }
                }
            );
            progress.start();
        
        // PAUSING
        }else{
            try{
                progress.stop();
                progress.interrupt();
                progress = null;
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Error2 progress pausing "+ e);
            }
        }
    }
